from sklearn.datasets import load_iris
from sklearn.linear_model import LogisticRegression
from sklearn.model_selection import train_test_split

data, target = load_iris(return_X_y=True)

len(data)

data_train, data_test, target_train, target_test = train_test_split(data,target, test_size=0.33)

model =  LogisticRegression(random_state=0, solver='lbfgs', 
                            multi_class='multinomial',max_iter=1000)

"""Training!"""

model.fit(data_train,target_train)

score1=model.score(data_train,target_train)

print(score1)

score2=model.score(data_test,target_test)

print(score2)

data_test[0]

predicted_results=model.predict([[2.3,5.8,7.2,4.5]])

